    private void registrarSede(){
        String []datos = new String[2];
        
        if(fieldDireccion.getText().length()!=0
                && fieldBarrio.getText().length()!=0
                && fieldCiudad.getText().length()!=0
                && fieldTelefono.getText().length()!=0){
            try{
                Statement leer = conect.createStatement();
                ResultSet resultado = leer.executeQuery("SELECT direccion_sede, barrio_sede FROM sede");

                while(resultado.next()){
                    datos[0]=resultado.getString(1);
                    datos[1]=resultado.getString(2);
                }
                
                if(!datos[0].equals(fieldDireccion.getText())
                        || !datos[1].equals(fieldBarrio.getText())){
                    PreparedStatement guardar = conect.prepareStatement("INSERT INTO sede(direccion_sede, barrio_sede, ciudad_sede, telefono_sede) VALUES(?,?,?,?)");
                    guardar.setString(1, fieldDireccion.getText());
                    guardar.setString(2, fieldBarrio.getText());
                    guardar.setString(3, fieldCiudad.getText());
                    guardar.setString(4, fieldTelefono.getText());
                    guardar.executeUpdate();
                    enlace.cerrar();
                    JOptionPane.showMessageDialog(null, "Los datos se guardaron correctamente");
                    regresarMenu();
                }else{
                    JOptionPane.showMessageDialog(null, "Los datos no pueden ser iguales a los de otras sedes");
                }
            }catch(SQLException e){
                JOptionPane.showMessageDialog(null, "No se completo el registro "+ e);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Ningun campo puede estar vacio");
        }
    }
    
    private void regresarMenu(){
        MenuUsuario regresar=new MenuUsuario();
        regresar.setVisible(true);
        this.setVisible(false);
        try {
            enlace.cerrar();
        } catch (SQLException ex) {
            Logger.getLogger(RegistroSede.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
